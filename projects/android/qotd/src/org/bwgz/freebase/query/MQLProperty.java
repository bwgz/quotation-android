package org.bwgz.freebase.query;

public class MQLProperty {
	private String name;
	private Object value;
	
	public MQLProperty(String name, Object value) {
		this.name = name;
		this.value = value;
	}
	
	public MQLProperty() {
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
}
